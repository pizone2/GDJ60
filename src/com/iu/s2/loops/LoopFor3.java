package com.iu.s2.loops;

public class LoopFor3 {

	public static void main(String[] args) {
		//구구단 3단
		
		
		for(int i=1;i<=9;i++)	
			for(int j=1;j<=9;j++) {
				System.out.println(i+"*"+j+ "=" + i*j);
				
				
			}
			
			
				
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
